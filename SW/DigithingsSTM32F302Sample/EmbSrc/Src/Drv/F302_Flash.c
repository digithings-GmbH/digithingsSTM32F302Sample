#include "../Target/Target.h"
#include "../STM32F302_Gen/STM32F302_FLASH_Def.h"

void F302_FlashInit(void) {
	volatile sSTM32F302_FLASH * pSTM32F302_FLASH = (sSTM32F302_FLASH *)F302_FLASH_ADR;
	volatile uSTM32F302_FLASH_ACR STM32F302_FLASH_ACR;

	STM32F302_FLASH_ACR.All = STM32F302_FLASH_ACR_RESET_VALUE;
	STM32F302_FLASH_ACR.Bit.LATENCY = 1;
	pSTM32F302_FLASH->STM32F302_FLASH_ACR.All = STM32F302_FLASH_ACR.All;
}
